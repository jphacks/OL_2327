# 新しいデータを逐次的にプロットするための関数
def plot_data(data):
    plt.figure(figsize=(10, 10))
    for elem in data:
        point1, point2 = elem[0], elem[1]
        plt.plot([point1[0], point2[0]], [point1[1], point2[1]], c='black')
        print(point1, point2)

    plt.title("Sequential Line Plot")
    plt.xlabel("x-coordinate")
    plt.ylabel("y-coordinate")
    plt.grid(True)
    plt.show()

